package offer;
/*
 * 我们可以用2*1的小矩形横着或者竖着去覆盖更大的矩形。
 * 请问用n个2*1的小矩形无重叠地覆盖一个2*n的大矩形，总共有多少种方法？
 */
public class MatrixCover {
	 public int RectCover(int target) {
	        int pre=2,preP=1;
	        if(target==1)
	            return 1;
	        if(target==2)
	            return 2;
	        int count=3,result=0;
	        while(count<=target){
	            count++;
	            result=pre+preP;
	            preP=pre;
	            pre=result;
	        }
	        return result;
	    }

}
